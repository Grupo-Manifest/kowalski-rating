class Evaluation:
    def __init__(self) -> None:
        self.rating = None
        self.review = None

        def set_rating(self, rating:int):
            self.rating = rating

        def get_rating(self):
            return self.rating 
        
        def set_review(self, review:str):
            self.review = review

        def get_review(self):
            return self.review